<template>
    <Cascader
        :value="form.model[field.model]"
        :data="computedOptions"
        :disabled="field.disabled"
        :clearable="field.clearable"
        :placeholder="field.placeholder"
        :filterable="true"
        :change-on-select="changeOnSelect"
        :size="size"
        @on-change="handleChange"
    />
</template>
<script>
import getOptions from '../mixins/getOptions';
export default {
    inject: ['form'],
    mixins: [getOptions],
    props: {
        field: {
            type: Object,
            required: true
        },
        size: {
            type: String,
            default() {
                return 'default';
            }
        },
    },
    data() {
        return {
            loading: false,
            options: []
        };
    },
    computed: {
        remote() {
            return !!this.field.api;
        },
        filterable() {
            return !!this.field.api || this.field.filterable;
        },
        computedOptions() {
            return this.options.length > 0 ? this.options : (this.field.options || this.field.data);
        },
        changeOnSelect() {
            if (this.field.changeType === undefined) {
                return false;
            }
            return this.field.changeType !== 'all';
        }
    },

    methods: {
        handleChange(value) {
            this.$set(this.form.model, this.field.model, value);
            this.$emit('on-change', this.field.model, value, null, this.field);
        }
    }
};
</script>
